"""
uvicorn 启动脚本。

双击运行或在命令行执行：
    python run.py

即可启动 FastAPI 开发服务器。
支持热重载（reload=True），修改代码后自动重启。
"""

from typing import Final

import uvicorn

# ============================================================================
# 模块级常量配置（使用 Final 确保不可变）
# ============================================================================

# FastAPI 应用模块路径：指向 app/main.py 中的 app 实例
APP_MODULE: Final[str] = "app.main:app"

# 监听地址：0.0.0.0 表示监听所有网络接口
HOST: Final[str] = "0.0.0.0"

# 端口号：使用 8001 避免与 NIM Docker 的 8000 端口冲突
PORT: Final[int] = 8001

# 热重载开关：关闭热重载，避免 WatchFiles 检测到文件变更后自动重启服务
RELOAD: Final[bool] = False

# ============================================================================
# 公开 API 声明
# ============================================================================

__all__ = ["main"]


# ============================================================================
# 主函数
# ============================================================================


def main() -> None:
    """
    启动 FastAPI 开发服务器。

    使用 uvicorn 运行应用，支持热重载功能。
    适用于开发环境，生产环境建议使用 gunicorn + uvicorn worker。
    """
    uvicorn.run(
        APP_MODULE,  # FastAPI 应用实例
        host=HOST,  # 监听地址
        port=PORT,  # 监听端口
        reload=RELOAD,  # 热重载配置
    )


if __name__ == "__main__":
    main()
